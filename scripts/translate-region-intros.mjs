#!/usr/bin/env node
/**
 * Phase B9.5 — Region-intro translator.
 *
 * Fills missing (country × region × locale) entries in
 * src/data/eu/regionIntrosGenerated.ts using the English baseline in
 * src/data/eu/regionIntros.ts as the source. Authored locale entries are
 * never overwritten.
 *
 *   bun scripts/translate-region-intros.mjs
 *   bun scripts/translate-region-intros.mjs --country=fr
 *   bun scripts/translate-region-intros.mjs --region=ile-de-france --locale=es
 *   bun scripts/translate-region-intros.mjs --provider=gemini
 *   bun scripts/translate-region-intros.mjs --dry
 */

import { writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { callAiJson, hasAnyKey } from "./_aiTranslate.mjs";

const ROOT = resolve(import.meta.dirname ?? ".", "..");
const INTROS_MODULE = resolve(ROOT, "src/data/eu/regionIntros.ts");
const GENERATED_PATH = resolve(ROOT, "src/data/eu/regionIntrosGenerated.ts");
const REGIONS_MODULE = resolve(ROOT, "src/data/eu/regions.ts");

const LOCALES = ["en", "de", "fr", "es", "it", "pt"];
const NATIVE = { de: "de", fr: "fr", es: "es", it: "it", pt: "pt" };
const LOCALE_NAME = {
  en: "English", de: "German", fr: "French", es: "Spanish", it: "Italian", pt: "Portuguese",
};
const COUNTRY_NAME = {
  de: "Germany", fr: "France", es: "Spain", it: "Italy", pt: "Portugal",
};

const args = Object.fromEntries(
  process.argv.slice(2).map((a) => {
    const [k, v] = a.replace(/^--/, "").split("=");
    return [k, v ?? true];
  }),
);
const ONLY_COUNTRY = args.country;
const ONLY_LOCALE = args.locale;
const ONLY_REGION = args.region;
const DRY = !!args.dry;
const PROVIDER = args.provider; // "lovable" | "gemini" | undefined

if (!hasAnyKey()) {
  console.error("Need LOVABLE_API_KEY or GEMINI_API_KEY in the environment.");
  process.exit(1);
}

async function main() {
  const { REGION_INTROS } = await import(INTROS_MODULE);
  const { GENERATED_REGION_INTROS } = await import(GENERATED_PATH);
  const { euRegions } = await import(REGIONS_MODULE);

  const generated = structuredClone(GENERATED_REGION_INTROS);
  const totals = { calls: 0, filled: 0, skipped: 0, errors: 0 };

  for (const region of euRegions) {
    const { country, canonical } = region;
    if (ONLY_COUNTRY && country !== ONLY_COUNTRY) continue;
    if (ONLY_REGION && canonical !== ONLY_REGION) continue;

    const intro = REGION_INTROS[country]?.[canonical];
    if (!intro || !intro.en) {
      console.log(`✗ ${country}/${canonical} — no English source, skipping`);
      continue;
    }
    const native = NATIVE[country];

    for (const locale of LOCALES) {
      if (locale === "en" || locale === native) continue;
      if (ONLY_LOCALE && locale !== ONLY_LOCALE) continue;
      if (intro[locale]) { totals.skipped += 1; continue; }
      if (generated[country]?.[canonical]?.[locale]) { totals.skipped += 1; continue; }

      const regionName = region.name[locale] || region.name.en;
      const countryName = COUNTRY_NAME[country];
      console.log(`→ ${country}/${canonical}/${locale} (${regionName})`);
      if (DRY) continue;

      const system = [
        `You translate concise editorial overviews of legal infrastructure in EU regions from English into ${LOCALE_NAME[locale]}.`,
        "Rules:",
        "- Preserve all proper nouns (court names, bar chamber names, statute names, place names) in their official form for the target language. Where the official name in the target language exists, use it; otherwise keep the original.",
        "- Preserve URLs, numeric figures and statute abbreviations verbatim.",
        "- Keep tone editorial and concise. Match source length within ±15%.",
        "- Output ONLY valid JSON: { \"text\": \"…\" }. No prose, no markdown.",
      ].join("\n");

      const user = JSON.stringify({
        targetLanguage: LOCALE_NAME[locale],
        region: regionName,
        country: countryName,
        source: intro.en,
      });

      try {
        const parsed = await callAiJson({ system, user, provider: PROVIDER });
        const text = typeof parsed?.text === "string" ? parsed.text.trim() : "";
        if (!text) throw new Error("empty translation");
        generated[country] = generated[country] ?? {};
        generated[country][canonical] = generated[country][canonical] ?? {};
        generated[country][canonical][locale] = text;
        totals.calls += 1;
        totals.filled += 1;
        writeGenerated(generated);
      } catch (err) {
        totals.errors += 1;
        console.error(`  ✗ ${country}/${canonical}/${locale}: ${err.message || err}`);
      }
    }
  }

  console.log(
    `\nDone. filled=${totals.filled} skipped=${totals.skipped} errors=${totals.errors} dry=${DRY}`,
  );
}

function writeGenerated(generated) {
  const header = `/**
 * AUTO-GENERATED by scripts/translate-region-intros.mjs — do not edit by hand.
 *
 * Phase B9.5 — Region-intro 6-locale fan-out overlay. Authored copy in
 * regionIntros.ts always wins; this file fills locale gaps via AI translation.
 */

import type { EuCountryCode, LocaleCode } from "./countries";

export type GeneratedRegionIntros = Partial<
  Record<EuCountryCode, Partial<Record<string, Partial<Record<LocaleCode, string>>>>>
>;

export const GENERATED_REGION_INTROS: GeneratedRegionIntros = `;
  writeFileSync(GENERATED_PATH, header + JSON.stringify(generated, null, 2) + ";\n", "utf8");
}

main().catch((err) => { console.error(err); process.exit(1); });
